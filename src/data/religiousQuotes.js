// Get a random quote for a specific religion and context
export const getRandomQuote = (religion, context = 'general') => {
    const religionQuotes = religiousQuotes[religion] || religiousQuotes.generic
    const contextQuotes = religionQuotes[context] || religionQuotes.general || []

    if (contextQuotes.length === 0) {
        // Fallback to general quotes
        const generalQuotes = religionQuotes.general || religiousQuotes.generic.general
        return generalQuotes[Math.floor(Math.random() * generalQuotes.length)]
    }

    return contextQuotes[Math.floor(Math.random() * contextQuotes.length)]
}

// Get all quotes for a religion
export const getQuotesByReligion = (religion) => {
    return religiousQuotes[religion] || religiousQuotes.generic
}
