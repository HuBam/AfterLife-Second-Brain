/**
 * Get upcoming holidays for a religion
 * @param {string} religion - Religion ID
 * @param {number} limit - Max number of results
 * @returns {Array} Upcoming holidays
 */
export const getUpcomingHolidays = (religion, limit = 5) => {
    const holidays = religiousHolidays[religion] || []
    const today = new Date()
    today.setHours(0, 0, 0, 0)

    return holidays
        .filter(h => new Date(h.date) >= today)
        .sort((a, b) => new Date(a.date) - new Date(b.date))
        .slice(0, limit)
}

/**
 * Get countdown to next major event
 * @param {string} religion - Religion ID
 * @returns {Object|null} Next major event with days remaining
 */
export const getCountdownToNext = (religion) => {
    const upcoming = getUpcomingHolidays(religion, 10)
    const major = upcoming.find(h => h.type === 'major')

    if (!major) return null

    const today = new Date()
    today.setHours(0, 0, 0, 0)
    const eventDate = new Date(major.date)
    const daysRemaining = Math.ceil((eventDate - today) / (1000 * 60 * 60 * 24))

    return {
        ...major,
        daysRemaining,
    }
}

/**
 * Get today's weekly reminders
 * @param {string} religion - Religion ID
 * @returns {Array} Today's reminders
 */
export const getTodayReminders = (religion) => {
    const reminders = weeklyReminders[religion] || []
    const days = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday']
    const today = days[new Date().getDay()]

    return reminders.filter(r => r.day === today)
}

/**
 * Check if today is Friday (for special reminders)
 */
export const isFriday = () => new Date().getDay() === 5
